-- ----------------------------
-- Poliklinikler Tablo Oluştur
-- ----------------------------
DROP TABLE IF EXISTS Poliklinikler;

create table Poliklinikler
(
PoliklinikID int Primary Key identity(1,1),
PoliklinikAdi nvarchar(50),
RandevuSure time(4)
)
-- ----------------------------
-- Poliklinik-Hastane Ara Tablosu Oluştur
