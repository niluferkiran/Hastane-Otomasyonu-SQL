-- ----------------------------
-- İlçeler Tablo Oluştur
-- ----------------------------
DROP TABLE IF EXISTS Ilceler;

CREATE TABLE Ilceler
(
  IlceID INT PRIMARY KEY IDENTITY(1,1) ,
