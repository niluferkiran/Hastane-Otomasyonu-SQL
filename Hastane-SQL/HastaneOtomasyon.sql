-- ----------------------------
-- Hastalar Tablo Oluştur
-- ----------------------------
DROP TABLE IF EXISTS Hastalar ;

CREATE table Hastalar
(
HastaID int primary key identity(1,1),
HastaAdi nvarchar(50),
HastaSoyadi nvarchar(50),
HastaAdres nvarchar(100),
HastaTelefon nvarchar(15),
