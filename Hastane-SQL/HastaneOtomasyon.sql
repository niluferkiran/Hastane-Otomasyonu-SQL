create table HastanePoliklinikDetay
(
PoliklinikID int,
HastaneID int
)
-- ----------------------------
-- Unvanlar Tablo Oluştur
-- ----------------------------
DROP TABLE IF EXISTS Unvanlar;

create table Unvanlar
(
UnvanID int primary key identity (1,1),
UnvanAdi nvarchar(50),
)
-- ----------------------------
-- Personel Tablo Oluştur
-- ----------------------------
DROP TABLE IF EXISTS Personeller ;

create table Personeller
(
PersonelID int primary key identity(1,1),
PersonelAdi nvarchar(50),
PersonelSoyadi nvarchar(50),
DiplomaNo nvarchar(20),
