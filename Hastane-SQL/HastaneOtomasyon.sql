-- ----------------------------
-- Hastaneler Tablo Oluştur
-- ----------------------------
DROP TABLE IF EXISTS Hastaneler;

create table Hastaneler
(
HastaneID int Primary Key identity(1,1),
HastaneAdi nvarchar(50) NOT NULL,
Telefon varchar(12),
IlID int,
Adres nvarchar(100)
