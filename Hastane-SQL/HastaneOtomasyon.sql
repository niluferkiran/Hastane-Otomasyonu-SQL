-- ----------------------------
-- Reçeteler Tablo Oluştur
-- ----------------------------
DROP TABLE IF EXISTS Receteler ;

create table Receteler 
(
ReceteId int PRIMARY KEY identity(1,1),
Barkod varchar(11),
)

